    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        try{
            if(this.idAlteracao == 0){
                compra = new Compra();
                compra.setFuncionario((Funcionario)this.getFuncionarioSelecionada(comboFuncionario.getSelectedIndex()));
                compra.setCliente((Cliente)this.getClienteSelecionado(comboCliente.getSelectedIndex()));
                compra.setProduto((Produto)this.getProdutoSelecionado(comboProduto.getSelectedIndex()));
                
                compraRN.salvar(compra);
                
            }else{
                compra.setFuncionario((Funcionario)this.getFuncionarioSelecionada(comboFuncionario.getSelectedIndex()));
                compra.setCliente((Cliente)this.getClienteSelecionado(comboCliente.getSelectedIndex()));
                compra.setProduto((Produto)this.getProdutoSelecionado(comboProduto.getSelectedIndex()));
                
                compraRN.atualizar(compra);
                
            }
            JOptionPane.showMessageDialog(this, "Compra cadastrado com sucesso!");
            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_btSalvarActionPerformed

