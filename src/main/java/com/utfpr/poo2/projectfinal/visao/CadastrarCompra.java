    private void popularComboFuncionario() {
        ComboBoxModel modeloCat;
            try {
                Vector linhas = new Vector();
                funcionarios = funcionarioRN.listaTodos();
                
                for (Funcionario f : funcionarios)
                {
                    Vector linha = new Vector();
                    linha.add(f.getCodigo());
                    linha.add(f.getNome());
                    linhas.add(linha);
                }
                modeloCat = new DefaultComboBoxModel(linhas);
                comboFuncionario.setModel(modeloCat);
            } catch (Exception e) {

                    e.printStackTrace();
            }
    }
    
    private void popularComboFuncionarioEdicao(Long id) {
        ComboBoxModel modeloCat;
            try {
                Vector linhas = new Vector();
                funcionarios = funcionarioRN.listaTodos();
                int index = 0;
                int indexSelecionado = 0;
                for (Funcionario c : funcionarios)
                {
                    Vector linha = new Vector();
                    linha.add(c.getCodigo());
                    linha.add(c.getNome());
                    linhas.add(linha);
                    
                    if (compra.getFuncionario().getCodigo() == (c.getCodigo())) {
                        indexSelecionado = index;
                    }
                    index++;
                }
                modeloCat = new DefaultComboBoxModel(linhas);
                comboFuncionario.setModel(modeloCat);
                comboFuncionario.setSelectedIndex(indexSelecionado);
            } catch (Exception e) {

                    e.printStackTrace();
            }
    }
    
