    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        // TODO add your handling code here:
        String filtro = txtNome.getText();
        
        try {
            DefaultTableModel model = (DefaultTableModel) jtCargo.getModel();
            model.setNumRows(0);
            this.cargos = cargoRN.listarFitroLike(filtro);
            
            for (Cargo cat : cargos) {
                String[] linha = {String.valueOf( cat.getCodigo() ), cat.getNome() };
                model.addRow(linha);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
            
    }//GEN-LAST:event_btPesquisarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed

        try{
            int linha = this.jtCargo.getSelectedRow();
            Long id = Long.valueOf(jtCargo.getValueAt(linha, 0).toString());
            
            Cargo catRecuperada = cargoRN.listarUm(id);
            cargoRN.excluir(catRecuperada);

            DefaultTableModel model = (DefaultTableModel) jtCargo.getModel();
            model.removeRow(linha);
            JOptionPane.showMessageDialog(this, "Cargo exclu√≠do com sucesso!");
            this.setClosable(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    
    
    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed

        int linha = this.jtCargo.getSelectedRow();
        Long id = Long.parseLong(jtCargo.getValueAt(linha, 0).toString());
        CadastrarCargo cadastrarCargo = new CadastrarCargo(id);
        this.jdpPrincipal.add(cadastrarCargo);
        cadastrarCargo.setVisible(true);
    }//GEN-LAST:event_btAlterarActionPerformed

    

