    private void popularComboFornecedor() {
        ComboBoxModel modeloMod;
            try {
                Vector linhas = new Vector();
                fornecedors = fornecedorRN.listaTodos();
                
                for (Fornecedor m : fornecedors)
                {
                    Vector linha = new Vector();
                    linha.add(m.getCodigo());
                    linha.add(m.getNome_fantasia());
                    linhas.add(linha);
                }
                modeloMod = new DefaultComboBoxModel(linhas);
                comboFornecedor.setModel(modeloMod);
            } catch (Exception e) {
                e.printStackTrace();
            }
    }
    
    private void popularComboFornecedorEdicao(Long id) {
        ComboBoxModel modeloMod;
            try {
                Vector linhas = new Vector();
                fornecedors = fornecedorRN.listaTodos();
                int index = 0;
                int indexSelecionado = 0;
                for (Fornecedor m : fornecedors)
                {
                    Vector linha = new Vector();
                    linha.add(m.getCodigo());
                    linha.add(m.getNome_fantasia());
                    linhas.add(linha);
                    
                    if(produto.getFornecedor().getCodigo() == m.getCodigo()){
                        indexSelecionado = index;
                    }
                    index++;
                }
                modeloMod = new DefaultComboBoxModel(linhas);
                comboFornecedor.setModel(modeloMod);
                comboFornecedor.setSelectedIndex(indexSelecionado);
            } catch (Exception e) {
                e.printStackTrace();
            }
    }
    private Object getFornecedorSelecionado(int posicao) 
    {
        Fornecedor m = new Fornecedor();
        m = (Fornecedor) fornecedors.get(posicao);

        return m;
    }

