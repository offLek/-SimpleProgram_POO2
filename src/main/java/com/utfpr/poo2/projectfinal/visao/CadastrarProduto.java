    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        try{
            if(this.idAlteracao == 0){
                produto = new Produto();
                produto.setFornecedor((Fornecedor)this.getFornecedorSelecionado(comboFornecedor.getSelectedIndex()));
                
                produto.setNome(txtNome.getText());
                produto.setQuantidade(new Integer(txtObservacao.getText()));
                produto.setPreco(new Double(txtValor.getText()));
                
                produtoRN.salvar(produto);
                
            }else{
                produto.setFornecedor((Fornecedor)this.getFornecedorSelecionado(comboFornecedor.getSelectedIndex()));
                
                produto.setNome(txtNome.getText());
                produto.setQuantidade(new Integer(txtObservacao.getText()));
                produto.setPreco(new Double(txtValor.getText()));
                
                produtoRN.atualizar(produto);
                
            }
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!");
            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_btSalvarActionPerformed

